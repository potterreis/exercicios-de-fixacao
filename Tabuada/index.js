// Faça um programa que mostre as tabuadas dos números de 1 a 10 usando laços de repetição.

let tabuada = 0

for(let i = tabuada; i <= tabuada + 10; i++ ){
    console.log("Tabuada do numero: " + i)
    for(let tabCont = 0; tabCont <= 10; tabCont++){
        console.log(i + " * " + tabCont + " = " + (i * tabCont))
    }
}